from lab04 import printMaze
from lab04 import solveMaze
from Stack import Stack


def test_example():
    maze = [
['+','+','G','+'],
['+','+',' ','+'],
['+',' ',' ','+'],
['+',' ','+','+'],
['+','+','+','+'] ]
	
    assert solveMaze(maze, 3, 1) == True
    assert maze == [
['+','+','G','+'],
['+','+', 4,'+'],
['+', 2,  3,'+'],
['+', 1,'+','+'],
['+','+','+','+'] ]



    maze = [
['+', 'G', '+', '+'],
['+', ' ', ' ', '+'],
['+', ' ', ' ', '+'],
['+', '+', '+', '+']]
    assert solveMaze(maze, 2, 2) == True
    assert maze == [
['+', 'G', '+', '+'],
['+',   3,   2, '+'],
['+', ' ',   1, '+'],
['+', '+', '+', '+']]

    

    maze = [
['+', '+', '+', '+'],
['+', ' ', ' ', '+'],
['+', ' ', ' ', '+'],
['+', '+', '+', '+']]
    assert solveMaze(maze, 2, 2) == False
    assert maze == [
['+', '+', '+', '+'],
['+', 3, 2, '+'],
['+', 4, 1, '+'],
['+', '+', '+', '+']]


    maze = [
['+','+','+','+'],
['+',' ',' ','+'],
['+',' ','G','+'],
['+',' ',' ','+'],
['+','+','+','+'] ]
	
    assert solveMaze(maze, 2, 2) == True
    assert maze == [
['+','+','+','+'],
['+',' ',' ','+'],
['+',' ','G','+'],
['+',' ',' ','+'],
['+','+','+','+'] ]
    

    maze = [
['+', '+', '+', '+', '+', '+'],
['+', ' ', ' ', '+', '+', '+'],
['+', 'G', ' ', ' ', ' ', ' '],
['+', '+', '+', '+', '+', '+']]
    assert solveMaze(maze, 2, 5) == True
    assert maze == [
['+', '+', '+', '+', '+', '+'],
['+',   6,   5, '+', '+', '+'],
['+', 'G',   4,   3,   2,   1],
['+', '+', '+', '+', '+', '+']]

